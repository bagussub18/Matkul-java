package Semester1.Modul10;

public class Praktik1 {
    public static void main(String[]args) {
        for (int a = 1; a <= 5; a++) {
            System.out.println("Ascending: "+a);
        }
        for (int a = 5; a >= 1; a--) {
            System.out.println("Descending: "+a);
        }
    }
}
